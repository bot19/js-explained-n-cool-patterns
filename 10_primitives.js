// 📝 String Data Type
"ecmascript";

// 🔢 Number Data Type
45;

// 💹 BigInt Data Type
9007199254740991n;

// 🚫 Null Data Type
null;
console.log(typeof null, "");  // Output will be "object"

// ❓ Undefined Data Type
undefined;

// 🟢 Boolean Data Type
true; 

// 🔏 Symbol Data Type
Symbol();

// Non-Primitive Data Types

// 📦 Object Data Type
const obj = {
    value: "ecmascript",
};

typeof obj.value; // Output will be "string"

// 📦 Arrat Data Type
const list = [];
typeof list; // Output will be "object"
